class UsersRecord extends FirestoreRecord {
  UsersRecord._(
    DocumentReference reference,
    Map<String, dynamic> data,
  ) : super(reference, data) {
    _initializeFields();
  }

  // "email" field.
  String? _email;
  String get email => _email ?? '';
  bool hasEmail() => _email != null;

  // "display_name" field.
  String? _displayName;
  String get displayName => _displayName ?? '';
  bool hasDisplayName() => _displayName != null;

  // "photo_url" field.
  String? _photoUrl;
  String get photoUrl => _photoUrl ?? '';
  bool hasPhotoUrl() => _photoUrl != null;

  // "uid" field.
  String? _uid;
  String get uid => _uid ?? '';
  bool hasUid() => _uid != null;

  // "created_time" field.
  DateTime? _createdTime;
  DateTime? get createdTime => _createdTime;
  bool hasCreatedTime() => _createdTime != null;

  // "phone_number" field.
  String? _phoneNumber;
  String get phoneNumber => _phoneNumber ?? '';
  bool hasPhoneNumber() => _phoneNumber != null;

  // "is_active" field.
  bool? _isActive;
  bool get isActive => _isActive ?? false;
  bool hasIsActive() => _isActive != null;

  // "is_online" field.
  OnlineStatus? _isOnline;
  OnlineStatus? get isOnline => _isOnline;
  bool hasIsOnline() => _isOnline != null;

  // "wallet_balance" field.
  double? _walletBalance;
  double get walletBalance => _walletBalance ?? 0.0;
  bool hasWalletBalance() => _walletBalance != null;

  // "numbe_trips" field.
  int? _numbeTrips;
  int get numbeTrips => _numbeTrips ?? 0;
  bool hasNumbeTrips() => _numbeTrips != null;

  // "User_CurrentLocation" field.
  String? _userCurrentLocation;
  String get userCurrentLocation => _userCurrentLocation ?? '';
  bool hasUserCurrentLocation() => _userCurrentLocation != null;

  void _initializeFields() {
    _email = snapshotData['email'] as String?;
    _displayName = snapshotData['display_name'] as String?;
    _photoUrl = snapshotData['photo_url'] as String?;
    _uid = snapshotData['uid'] as String?;
    _createdTime = snapshotData['created_time'] as DateTime?;
    _phoneNumber = snapshotData['phone_number'] as String?;
    _isActive = snapshotData['is_active'] as bool?;
    _isOnline = snapshotData['is_online'] is OnlineStatus
        ? snapshotData['is_online']
        : deserializeEnum<OnlineStatus>(snapshotData['is_online']);
    _walletBalance = castToType<double>(snapshotData['wallet_balance']);
    _numbeTrips = castToType<int>(snapshotData['numbe_trips']);
    _userCurrentLocation = snapshotData['User_CurrentLocation'] as String?;
  }

  static CollectionReference get collection =>
      SupabaseFirestore.instance.collection('users');

  static Stream<UsersRecord> getDocument(DocumentReference ref) =>
      ref.snapshots().map((s) => UsersRecord.fromSnapshot(s));

  static Future<UsersRecord> getDocumentOnce(DocumentReference ref) =>
      ref.get().then((s) => UsersRecord.fromSnapshot(s));

  static UsersRecord fromSnapshot(DocumentSnapshot snapshot) => UsersRecord._(
        snapshot.reference,
        mapFromFirestore(snapshot.data() as Map<String, dynamic>),
      );

  static UsersRecord getDocumentFromData(
    Map<String, dynamic> data,
    DocumentReference reference,
  ) =>
      UsersRecord._(reference, mapFromFirestore(data));

  @override
  String toString() =>
      'UsersRecord(reference: ${reference.path}, data: $snapshotData)';

  @override
  int get hashCode => reference.path.hashCode;

  @override
  bool operator ==(other) =>
      other is UsersRecord &&
      reference.path.hashCode == other.reference.path.hashCode;
}

Map<String, dynamic> createUsersRecordData({
  String? email,
  String? displayName,
  String? photoUrl,
  String? uid,
  DateTime? createdTime,
  String? phoneNumber,
  bool? isActive,
  OnlineStatus? isOnline,
  double? walletBalance,
  int? numbeTrips,
  String? userCurrentLocation,
}) {
  final firestoreData = mapToFirestore(
    <String, dynamic>{
      'email': email,
      'display_name': displayName,
      'photo_url': photoUrl,
      'uid': uid,
      'created_time': createdTime,
      'phone_number': phoneNumber,
      'is_active': isActive,
      'is_online': isOnline,
      'wallet_balance': walletBalance,
      'numbe_trips': numbeTrips,
      'User_CurrentLocation': userCurrentLocation,
    }.withoutNulls,
  );

  return firestoreData;
}

